import { Column, Entity, JoinColumn, ManyToOne } from "typeorm";

@Entity("message", { schema: "public" })
export class Message {
  @Column("uuid", {
    primary: true,
    name: "id",
    default: () => "uuid_generate_v4()",
  })
  id: string;

  @Column("timestamp without time zone", {
    name: "createdAt",
    default: () => "now()",
  })
  createdAt: Date;

  @Column("timestamp without time zone", {
    name: "updatedAt",
    default: () => "now()",
  })
  updatedAt: Date;

  @Column("text", { name: "text" })
  text: string;
}
